numbers=range(5)

for item in numbers:
    print(item)

# range (starting value including itself, ending value excluding itself)
print('')
numbers=range(5,10)

for item in numbers:
    print(item)

# range (starting value including itself, ending value excluding itself, skip steps)
print('')
numbers=range(5,10,2)

for item in numbers:
    print(item)

# without variable
print('')

for item in range(5):
    print(item)